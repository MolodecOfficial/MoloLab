function fillMissingDates(startDate: string, endDate: string, schedule: any) {
    const missingDates = [];

    // Преобразуем даты в формат Date для удобства работы
    const start = new Date(startDate);
    const end = new Date(endDate);

    // Перебираем все дни в промежутке между startDate и endDate
    for (let date = start; date <= end; date.setDate(date.getDate() + 1)) {
        const formattedDate = date.toISOString().split('T')[0]; // форматируем как YYYY-MM-DD
        if (!schedule[formattedDate]) {
            missingDates.push(formattedDate);
        }
    }

    // Добавляем "Нет занятий" для отсутствующих дат
    missingDates.forEach(date => {
        schedule[date] = {
            'БПИ, БПИ09з': [
                {
                    subject: 'Нет занятий',
                },
            ]
        };
    });

    return schedule;
}

